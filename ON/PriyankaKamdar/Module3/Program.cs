using System;

namespace Increment_Decrement
{
    class Program
    {
        static void Main(string[] args)
        {
            for (int a = 0; a <= 10; a++)  // Value of a is assigned with a condition of a <= 10 to get the numbers less than or equals to 10. 
                                          //  Also increment expresion a++ is used to loop the variable.
            {
                Console.WriteLine(a);
            }


        }
    }
}

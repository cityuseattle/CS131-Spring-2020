using System;

namespace Increment_Decrement
{
    class Program
    {
        static void Main(string[] args)
        {
            for (int a = 0; a <= 10; a++)
            {
                Console.WriteLine(a);
            }

            for (int a = 15; a >= 1; a--)
            {
                Console.WriteLine(a);
            }
        }
    }
}